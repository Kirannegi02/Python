# Variables are containers for storing data values.

x = 5
y = "Kiran"
print(x)
print(y)

#If we want to specify the data type of a variable, this can be done with casting.
x = str(6)
y = int(6)
z = float(6)
print(x)
print(y)
print(z)

# We can get the type of a variable with the type() function.
x = 9
y = "Kiran"
print(type(x))
print(type(y))

# Python allows us to assign many values to multiple variables.
x, y, z = "Red", "Blue", "Black"
print(x)
print(y)
print(z) 